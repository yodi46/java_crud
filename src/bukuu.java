    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            url="jdbc:mysql://"+ host +"/"+ db + "?user=" + user +"&password="+ pswd;
            Connection conn=DriverManager.getConnection(url);
            PreparedStatement pStatement =
            conn.prepareStatement("INSERT INTO buku(kode,judul,pengarang,penerbit,tahun,jumlah)" + "VALUES (?,?,?,?,?,?)");
            pStatement.setString(1, tkode.getText());
            pStatement.setString(2, tjudul.getText());
            pStatement.setString(3, tpengarang.getText());
            pStatement.setString(4, tpenerbit.getText());
            pStatement.setString(5, ttahun.getText());
            pStatement.setString(6, tjumlah.getText());

            if (pStatement.executeUpdate()>0)
            JOptionPane.showMessageDialog(this,
                "Penambahan sukses", "Informasi",
                JOptionPane.INFORMATION_MESSAGE);

            else
            JOptionPane.showMessageDialog(this,
                "Penambahan gagal", "Informasi",
                JOptionPane.INFORMATION_MESSAGE);
            tampilTabel();

            pStatement.close();
            conn.close();
            tkode.setText("");
            tjudul.setText("");
            tpengarang.setText("");
            tpenerbit.setText("");
            ttahun.setText("");
            tjumlah.setText("");
        }
        catch(ClassNotFoundException e) {
            System.out.println("jdbc.Driver tidak ditemukan");
        }
        catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btncekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncekActionPerformed
        try {
            Statement st = Lconnection.createStatement();
            ResultSet rs = st.executeQuery
            ("Select * from buku" + " where kode ='" + tkode.getText() + "'");
            if (rs.next()) {
                btnedit.setEnabled(true);
                tkode.setText(rs.getString("kode"));
                tjudul.setText(rs.getString("judul"));
                tpengarang.setText(rs.getString("pengarang"));
                tpenerbit.setText(rs.getString("penerbit"));
                ttahun.setText(rs.getString("tahun"));
                tjumlah.setText(rs.getString("jumlah"));
            } else {
                JOptionPane.showMessageDialog(this,"Kode: Salah",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
                btnedit.setEnabled(false);
                tkode.setText("");
                tjudul.setText("");
                tpengarang.setText("");
                tpenerbit.setText("");
                ttahun.setText("");
                tjumlah.setText("");
                tjudul.requestFocus();
            }
        }
        catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
    }//GEN-LAST:event_btncekActionPerformed

