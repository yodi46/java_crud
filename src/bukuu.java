   private Object[][] getData(){
        Object[][] data1= null;
        try {
        Statement st = Lconnection.createStatement();
        ResultSet rs = st.executeQuery("Select * from buku");
        rs.last();
        int rowCount= rs.getRow();
        rs.beforeFirst();
        data1= new Object[rowCount][6];
        int no=-1;
        while (rs.next()) {
        no=no+1;
        data1[no][0]=rs.getString("kode");
        data1[no][1]=rs.getString("judul");
        data1[no][2]=rs.getString("pengarang");
        data1[no][3]=rs.getString("penerbit");
        data1[no][4]=rs.getString("tahun");
        data1[no][5]=rs.getString("jumlah");
        }
        st.close();
        }
        catch (SQLException e){
        System.out.println("koneksi gagal " +
                e.toString());
        }
        return data1;
        }
        private void tampilTabel() {
        String[] columnNames = {"kode", "judul", "pengarang",
        "penerbit", "tahun", "jumlah"};
        table = new JTable(getData(), columnNames);
        jScrollPane1.setViewportView(table);
        }
