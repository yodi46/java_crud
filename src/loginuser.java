       private void CekLogin(){
        try{
            if(tid.getText().equals("") || tpass.getPassword().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Data Tidak Boleh Kosong", "Pesan", JOptionPane.ERROR_MESSAGE);
            tid.requestFocus();
            HapusLayar();
        }else{
            st = conn.createStatement();
            String sql = ("SELECT * FROM user WHERE id ='"+tid.getText()+
            "' AND password = '"+String.valueOf(tpass.getPassword())+"'");
            ResultSet rs = st.executeQuery(sql);
            if(rs.next()){
            this.dispose();
            new bukuu().setVisible(true);
            }else{
            JOptionPane.showMessageDialog(rootPane, "Id dan Password Salah\nAtau Akun Belum Terdaftar", "Pesan",JOptionPane.ERROR_MESSAGE);
            HapusLayar();
            }
            }
        }catch(Exception e){
            e.printStackTrace();
}
       }
       
       private void HapusLayar(){
        tid.setText("");
        tpass.setText("");
        tid.setEnabled(true);
        tpass.setEnabled(true);
        }

