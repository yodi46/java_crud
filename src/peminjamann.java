    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            url="jdbc:mysql://"+ host +"/"+ db + "?user=" + user +"&password="+ pswd;
            Connection conn=DriverManager.getConnection(url);
            PreparedStatement pStatement =
            conn.prepareStatement("INSERT INTO peminjaman()" + "VALUES (?,?,?,?,?,?)");
            pStatement.setString(1, tno.getText());
            pStatement.setString(2, tid.getText());
            pStatement.setString(3, tnpm.getText());
            pStatement.setString(4, tkode.getText());
            pStatement.setString(5, tjumlah.getText());
            pStatement.setString(6, ttglpinjam.getText());

            if (pStatement.executeUpdate()>0)
            JOptionPane.showMessageDialog(this,
                "Penambahan sukses", "Informasi",
                JOptionPane.INFORMATION_MESSAGE);

            else
            JOptionPane.showMessageDialog(this,
                "Penambahan gagal", "Informasi",
                JOptionPane.INFORMATION_MESSAGE);
            tampilTabel();

            pStatement.close();
            conn.close();
            tno.setText("");
            tid.setText("");
            tnpm.setText("");
            tkode.setText("");
            tjumlah.setText("");
            ttglpinjam.setText("");
        }
        catch(ClassNotFoundException e) {
            System.out.println("jdbc.Driver tidak ditemukan");
        }
        catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btncekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncekActionPerformed
        try {
            Statement st = Lconnection.createStatement();
            ResultSet rs = st.executeQuery
            ("Select * from peminjaman" + " where nopinjam ='" + tno.getText() + "'");
            if (rs.next()) {
                btnedit.setEnabled(true);
                tid.setText(rs.getString("id"));
                tnpm.setText(rs.getString("npm"));
                tkode.setText(rs.getString("kode"));
                tjumlah.setText(rs.getString("jumlah"));
                ttglpinjam.setText(rs.getString("tglpinjam"));
            } else {
                JOptionPane.showMessageDialog(this,"No peminjaman : Salah",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
                btnedit.setEnabled(false);
                tno.setText("");
                tid.setText("");
                tnpm.setText("");
                tkode.setText("");
                tjumlah.setText("");
                ttglpinjam.setText("");
                tno.requestFocus();
            }
        }
        catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
    }//GEN-LAST:event_btncekActionPerformed

